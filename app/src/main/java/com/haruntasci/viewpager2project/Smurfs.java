package com.haruntasci.viewpager2project;

public class Smurfs {

    private int image;
    private String text;

    public Smurfs(int image, String text) {
        this.image = image;
        this.text = text;
    }

    public int getImage() {
        return image;
    }

    public String getText() {
        return text;
    }
}
